import React from 'react';
import { Router, Route } from 'react-router-dom';
import { connect } from 'react-redux';

import { history } from '../_helpers';
import { alertActions } from '../_actions';
import { PrivateRoute } from '../_components';
import { HomePage } from '../HomePage';
import { LoginPage } from '../LoginPage';
import { RegisterPage } from '../RegisterPage';
import { GroupListPage } from '../GroupListPage';
import {CreateGroupPage} from "../CreateGroupPage";
import {RequestStatusPage} from "../RequestStatusPage";
import {AcceptRequestPage, ListUsersPage} from "../ListUsersPage";

class App extends React.Component {
    constructor(props) {
        super(props);

        const { dispatch } = this.props;
        history.listen((location, action) => {
            // clear alert on location change
            dispatch(alertActions.clear());
        });
    }

    render() {
        const { alert } = this.props;
        return (
            <div>
                        {alert.message &&
                            <div className={`alert ${alert.type}`}>{alert.message}</div>
                        }
                        <Router history={history}>
                            <div>
                                <PrivateRoute exact path="/" component={HomePage} />
                                <Route path="/login" component={LoginPage} />
                                <Route path="/group_list" component={GroupListPage} />
                                <Route path="/group_create" component={CreateGroupPage} />
                                <Route path="/request_status" component={RequestStatusPage} />
                                <Route path="/list_users" component={ListUsersPage} />
                                <Route path="/register" component={RegisterPage} />
                            </div>
                        </Router>
            </div>
        );
    }
}

function mapStateToProps(state) {
    const { alert } = state;
    return {
        alert
    };
}

const connectedApp = connect(mapStateToProps)(App);
export { connectedApp as App }; 